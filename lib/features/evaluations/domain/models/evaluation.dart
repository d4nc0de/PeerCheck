class Evaluation {
  final String id;
  final String evaluatorId;
  final String evaluatedId;
  final int puntualidad;
  final int contribucion;
  final int compromiso;
  final int actitud;

  Evaluation({
    required this.id,
    required this.evaluatorId,
    required this.evaluatedId,
    required this.puntualidad,
    required this.contribucion,
    required this.compromiso,
    required this.actitud,
  });
}
